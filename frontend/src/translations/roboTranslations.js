export function getRoboT(lang, key) {
  const parts = key.split('.');
  let obj = roboTranslations[lang] || roboTranslations.tr;
  for (const p of parts) {
    obj = obj?.[p];
    if (obj == null) break;
  }
  if (typeof obj === 'string') return obj;
  return roboTranslations.tr;
}

// Helper to get nested robo key - we'll use in component with language from context
export function resolveRoboKey(lang, key) {
  const parts = key.split('.');
  let obj = roboTranslations[lang] || roboTranslations.tr;
  for (const p of parts) {
    if (p === 'robo') continue;
    obj = obj?.[p];
  }
  return typeof obj === 'string' ? obj : key;
}
